func (d *backupsDataSource) Read(ctx context.Context, req datasource.ReadRequest, resp *datasource.ReadResponse) {
	var data backupsDataSourceData
	diags := req.Config.Get(ctx, &data)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// set default value
	var page int32 = 1
	var pageSize int32 = 10
	if !data.Page.IsNull() && !data.Page.IsUnknown() {
		page = int32(data.Page.ValueInt64())
	}
	if !data.PageSize.IsNull() && !data.PageSize.IsUnknown() {
		pageSize = int32(data.PageSize.ValueInt64())
	}

	tflog.Trace(ctx, "read backups data source")
	listBackUpOfClusterOK, err := d.provider.client.ListBackUpOfCluster(backupApi.NewListBackUpOfClusterParams().WithProjectID(data.ProjectId).WithClusterID(data.ClusterId).WithPage(&page).WithPageSize(&pageSize))
	if err != nil {
		resp.Diagnostics.AddError("Read Error", fmt.Sprintf("Unable to call ListBackups, got error: %s", err))
		return
	}

	data.Id = types.StringValue(strconv.FormatInt(rand.Int63(), 10))
	data.Total = types.Int64Value(listBackUpOfClusterOK.Payload.Total)
	var items []backup
	for _, key := range listBackUpOfClusterOK.Payload.Items {
		items = append(items, backup{
			Id:              key.ID,
			Description:     key.Description,
			Name:            key.Name,
			Type:            key.Type,
			Size:            key.Size,
			CreateTimestamp: key.CreateTimestamp.String(),
			Status:          key.Status,
		})
	}
	data.Items = items

	diags = resp.State.Set(ctx, &data)
	resp.Diagnostics.Append(diags...)
}
